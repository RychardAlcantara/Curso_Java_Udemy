public class ConversaoNumeroString {

	public static void main(String[] args) {
		
		
		Integer num1 = 1000;
		System.out.println(num1);
		System.out.println("O valor tem " + num1.toString().length() + " caracteres");
		//.toString para declarar que o valor de num1 passara a ser uma String
		//.length para contar quantos caracteres tem nessa string
		
		

	}

}
